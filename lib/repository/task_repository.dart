import 'package:task_management/dao/task_dao.dart';
import 'package:task_management/model/task_model.dart';

class TaskRepository{
  final taskDao = TaskDao();

  Future getAllTodos({required String query}) => taskDao.getTodos();

  Future insertTodo(Task todo) => taskDao.createTodo(todo);

  Future updateTodo(Task todo) => taskDao.updateTodo(todo);

  Future deleteTodoById(int id) => taskDao.deleteTodo(id);

}